def superReducedString( s ):
    stk = [ s[ 0 ]]
    n = len( s )
    j = 1
    while j < n:
        ch = s[ j ]
        if stk and stk[ -1 ] == ch:
            stk.pop()
        else:
            stk.append( ch )
        j += 1
    if stk:
        return ''.join( stk )
    else:
        return "Empty String"
