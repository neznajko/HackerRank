# Given an array of integers and a target value, determine the
# number of pairs of array elements that have a difference equal
# to the target value
################################################################
# 2 ≤ n ≤ 10⁵
# 0 < k < 10⁹
# 0 < a[j] < 2³¹ - 1
# each integer a[j] is unique
################################################################
################################################################
################################################################
################################################################
# The fact that each integer is unique is nice be cos, otherwise
# we have to compute different combinations. Here we can use
# hashing techniques to determine the number of pairs:
# Say for two numbers x, y in a: x - y = ±k, then x, y will be
# such pair, so if we know x, then y must be equal to y = x ± k.
# So we can use a Set having all values of a, and then for each
# value x of a, we check if the Set has values x ± k, after that
# we have to divide by 2, be cos each pair is counted twice
#
# Let a = [ 3,5,4,2,1 ], and k = 2, the Set is s = [ 1,2,3,4,5 ],
# then we loop over a:
# x = 3, check for 3 ± 2: 5 and 1, total = 2
# x = 5, check for 5 ± 2: 3, total = 3
# x = 4, check for 4 ± 2: 2, total = 4
# x = 2, check for 2 ± 2: 4, total = 5
# x = 1, check for 1 ± 2: 3, total = 6
# number of pairs = total/2 = 3
#
################################################################
################################################################
################################################################
################################################################
################################################################
################################################################
################################################################
################################################################
################################################################
################################################################
################################################################
################################################################
################################################################
################################################################
################################################################
################################################################

def pairs( k, a ):
    s = set( a )
    total = 0
    for x in a:
        if x - k in s: total += 1
        if x + k in s: total += 1
    return total // 2

