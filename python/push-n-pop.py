def appendAndDelete( s, t, k ):
    m = min( len( s ), len( t ))
    j = 0
    while j < m and s[ j ] == t[ j ]: j += 1
    k -= len( s ) - j # nf pops
    k -= len( t ) - j # nf pushes
    if k ==    0: return "Yes"
    if k     < 0: return "No"
    if k > 2 * j: return "Yes"
    if k   &   1: return "No"
    return "Yes"
