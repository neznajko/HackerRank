def fairRations( B ):
    s = len( B )
    c = 0
    for j in range( s - 1 ):
        if isodd( B[ j ]):
            B[ j + 1 ] += 1
            c += 2
    return ( str( c ), "NO" )[ isodd( B[ s - 1 ])]
