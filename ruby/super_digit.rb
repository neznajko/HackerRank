def get_super_digit( n ) 
    return n if n / 10 == 0
    get_super_digit( digit_sum( n ))
end
################################################################
################################################################
################################################################
################################################################
def digit_sum( n )
    sum = 0
    while n > 0 do
        sum += n % 10
        n /= 10
    end
    sum
end
