def cutTheSticks( a )
    h = Hash.new( 0 ) #          dictionary with default value 0
    a.each do | v |
        h[ v ] += 1
    end
    keys = h.keys.sort
    n = a.length
    r = [] #                                the remaining sticks
    keys.each do | key |
        r.push( n )
        n -= h[ key ]
    end
    r
end
