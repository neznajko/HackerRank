def encryption( s ) #             doo de su ka # どうですか
  s.gsub!( /\s/, '' ) #                      g # wie ist
  m = Math.sqrt( s.length ).ceil #           s # cómo es
  a = Array.new( m ){ '' } #                 i # com'è
  s.each_char.with_index do | c, i | #       u # як
    a[ i % m ] << c #                        f # comment est
  end #                                      e # how is
  return a.join(' ') #                         #
end #                                          #
