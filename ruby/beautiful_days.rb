def reverse( n )
    digits = []
    while n > 0 do
        digits.push( n % 10 )
        n /= 10
    end
    r = 0
    factor = 1
    while !digits.empty? do
        digit = digits.pop
        r += factor * digit
        factor *= 10
    end
    r
end
