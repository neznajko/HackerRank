def hackerrankInString( s )                                 
    keystr = "hackerrank"
    offset = 0
    s.each_char do | ch |
        if ch == keystr[ offset ]
            offset += 1
            if offset == keystr.length
                return :YES
            end
        end
    end
    :NO
end
