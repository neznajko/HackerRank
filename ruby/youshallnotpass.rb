def dgit?( ch )
    '0' <= ch and ch <= '9'
end
################################################################
def lcas?( ch )
    ch.downcase == ch
end
################################################################
def ucas?( ch )
    ch.upcase == ch
end
################################################################
def spec?( ch )
    "!@#$%^&*()-+".include?( ch )
end
################################################################
################################################################
################################################################
################################################################
FUNC = [ method( :dgit? ),
         method( :spec? ),
         method( :lcas? ), 
         method( :ucas? )]
################################################################
################################################################
MINLEN = 6
################################################################
################################################################
def minimumNumber( n, pas )
    counter = [ 0, 0, 0, 0 ]
    pas.each_char do |ch|
        FUNC.each_with_index do |f,j|
            if f.call( ch )
                counter[ j] += 1
                break
            end
        end
    end
    not_possible = 0
    counter.each do |c|
        not_possible += 1 if c == 0
    end
    n += not_possible
    if n < MINLEN
        return not_possible + MINLEN - n
    end
    not_possible
end
