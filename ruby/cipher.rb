def rotate( ch, k )
    if lowerletter?( ch )
        base = 'a'.ord
    elsif upperletter?( ch )
        base = 'A'.ord
    else
        return ch
    end
    off = ( ch.ord - base + k ) % ABSIZE
    ( base + off ).chr
end
