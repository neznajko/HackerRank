def makehash( k )
    # defaults to key if not set, that is h[ '-' ] = '-'
    h = Hash.new {| hash, key | key }
    for ch in "a" .. "z" 
        h[ ch ] = rotate( ch, k )    
    end
    for ch in "A" .. "Z"
        h[ ch ] = rotate( ch, k )
    end
    h
end
################################################################
################################################################
def caesarCipher( s, k )
    h = makehash( k )
    cipher = ""
    s.each_char do | ch |
        cipher << h[ ch ]
    end
    cipher
end
