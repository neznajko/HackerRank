def surfaceArea( a )
    n = a.length
    m = a[ 0 ].length
    total = surf( a[0][0] )
    for j in ( 1 .. m - 1 ) do
        total += surf( a[0][j] ) - commn( a[0][j - 1], a[0][j] )
    end
    for i in ( 1 .. n - 1 ) do
        total += surf( a[i][0] ) - commn( a[i - 1][0], a[i][0] )
    end
    for i in ( 1 .. n - 1 ) do
        for j in ( 1 .. m - 1 ) do
            total += surf(  a[ i ][ j ])
            total -= commn( a[ i ][ j ], a[ i - 1 ][ j ])
            total -= commn( a[ i ][ j ], a[ i ][ j - 1 ])
        end
    end
    total
end
