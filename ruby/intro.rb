def binary_search( key, a )
    l = 0
    u = a.length - 1
    while l <= u
        m = ( l + u ) >> 1
        return m if a[ m ] == key
        if a[ m ] < key
            l = m + 1
        else
            u = m - 1
        end
    end   
    l
end
################################################################
################################################################
def introTutorial( key, a )
    binary_search( key, a )
end
