# We define subsequence as any subset of an array. We 
# define a subarray as a contiguous subsequence in an 
# array. Given an array, find the maximum possible sum
# among:
# 
#   1) all nonempty subarrays
#   2) all nonempty subsequences
#
# Print the two values as space-separated integers on
# one line
#######################################################
#######################################################
#######################################################
#######################################################
# www.hackerrank.com/challenges/maxsubarray/problem
#######################################################
def maxSubarray( a )
    cons = 0 #                           contiguous sum
    nons = 0 #                        noncontiguous sum
    curs = 0 #                              current sum
             #
    a.each do |v| # らい年中国にかえります。
        curs += v
        curs = 0 if curs < 0
        cons = curs if cons < curs
        nons += v if v > 0
    end 
    if nons == 0
        cons = nons = a.max
    end
    [ cons, nons ]
end
fptr = $stdout ########################################
