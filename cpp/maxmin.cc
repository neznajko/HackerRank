int maxMin( int k, vector<int>& a ){
    const int n = a.size();
    sort( a.begin(), a.end() );
    int dif = a[n - 1] - a[0];
    for( int i = 0; i < n + 1 - k; ++i ){
        int j = i + k - 1;
        dif = min( dif, a[j] - a[i] );
    }
    return dif;
}
////////////////////////////////////////////////////////////////
int main() {
    int n; cin >> n;
    int k; cin >> k;
    vector<int> a( n );
    for( int& y: a ){ cin >> y; }
    cout << maxMin( k, a ) << endl;
}
