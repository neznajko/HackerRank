// Non-Divisible Subset
////////////////////////////////////////////////////////////////
// Given a set of distinct integers, print the size of a maximal 
// subset of S where the sum of any 2 numbers in S' is not 
// evenly divisible by k
////////////////////////////////////////////////////////////////
// www.hackerrank.com/challenges/non-divisible-subset/problem
////////////////////////////////////////////////////////////////
package main
////////////////////////////////////////////////////////////////
