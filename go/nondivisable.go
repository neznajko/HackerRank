// k = 9                                       k = 6
// 0 1 2 3 4 5 6 7 8                           0 1 2 3 4 5
//   1             8                             1       5  
//     2         7                                 2   4
//       3     6
//         4 5
// 
// times = ( 9 - 1 ) / 2 = 4                   ( 6 - 1 ) / 2 = 2
//
// i = 1, j = k - 1
// for t = ( k - 1 ) / 2; t > 0; t-- {
//      compare( i++, j-- )
// }
////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////
func nonDivisibleSubset( k int32, a []int32 ) int {
    reminders := make( []int, k )
    for _, value := range a {
        reminders[ value % k ]++;
    }
    s := 0
    if reminders[ 0 ] > 0 {
        s++
    }
    if k & 1 == 0 {
        if reminders[ k / 2 ] > 0 {
            s++
        }
    }
    i := 1
    j := k - 1
    for t := j / 2; t > 0; t-- {
        if reminders[ j ] > reminders[ i ] {
            s += reminders[ j ]
        } else {
            s += reminders[ i ]
        }
        i++
        j--
    }
    return s
}
